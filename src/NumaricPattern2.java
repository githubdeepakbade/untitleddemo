public class NumaricPattern2 
{
    public static void main(String[] args) 
    {
        for (int i = 1; i <=5 ; i++)
        {
            int count=0;
            for (int j = 1; j <=i ; j++)
            {
                count=count+1;
                System.out.print(count+" ");
            }
            System.out.println();
        }
        System.out.println("thank u");
    }
    
}
